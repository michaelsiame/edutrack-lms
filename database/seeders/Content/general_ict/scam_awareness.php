<h3>What is a scam?</h3>
<p>A <strong>scam</strong> is a dishonest trick used to steal your money or personal information. Scammers do not usually "hack" complicated computer systems. Instead, they trick <em>people</em>. This is called <strong>social engineering</strong>: they use fear, excitement, urgency or sympathy to make you act before you think. Their goal is to get you to send money, share a PIN or password, or give away information they can use to pretend to be you.</p>
<p>Scammers are skilled actors. They may sound friendly, professional or frightening. They may pretend to be from your bank, your mobile network, a government office, a potential employer or even a new friend. The most important thing to remember is that legitimate organisations will never ask you to share your PIN, OTP or password.</p>

<h3>Common scams in Zambia</h3>
<ul>
<li><strong>"You have won!"</strong> — An SMS or phone call says you have won a promotion, lottery, airtime or cash prize from a network or bank. To "claim" it you must send money or share a PIN. Real promotions never ask you to pay to receive a prize.</li>
<li><strong>Mobile-money "wrong number" trick</strong> — Someone claims they sent money to your number by mistake and begs you to send it back. The "deposit" message is fake; if you send money, you lose real money.</li>
<li><strong>Fake agent / "reverse the transaction"</strong> — A caller pretends to be from Airtel, MTN, Zamtel or a bank and says there is a problem with your account. They ask for your PIN, OTP or a code to "fix" it.</li>
<li><strong>Job and scholarship scams</strong> — You are offered a job or college place but must first pay a "registration," "processing" or "uniform" fee. Real employers do not ask you to pay to get a job.</li>
<li><strong>Phishing links</strong> — A message contains a link to a fake page that looks like your bank or Facebook, asking you to "log in" so the scammers can steal your password.</li>
<li><strong>SIM-swap fraud</strong> — A criminal tricks your network provider into transferring your number to their SIM card. They then receive your calls, SMS messages and OTPs.</li>
<li><strong>Romance and friendship scams</strong> — Someone you met online builds trust over days or weeks, then asks for money for a fake emergency.</li>
</ul>

<h3>Why scams succeed</h3>
<p>Scams succeed because they target normal human feelings: hope, fear, greed, kindness and respect for authority. A message saying you have won K5,000 appeals to hope. A message saying your account will be closed today creates fear. A caller claiming to be from your bank uses your respect for authority. Recognising these emotional triggers is the first step to protecting yourself.</p>

