<li><strong>Phishing</strong>: A fake message or website designed to trick you into giving away passwords or personal details.</li>
<li><strong>SIM-swap fraud</strong>: Transferring a victim's phone number to a criminal's SIM card to intercept calls and messages.</li>
</ul>

<h3>Summary</h3>
<p>Scammers use social engineering to manipulate emotions and trick people into giving away money or information. Knowing the common scams in Zambia helps you recognise them before you become a victim.</p>
