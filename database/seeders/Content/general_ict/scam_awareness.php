<p>Whenever someone asks for money, information or access to your account, verify the request through a separate channel:</p>
<ol>
<li><strong>Stop.</strong> Do not send money or share any code.</li>
<li><strong>Think.</strong> Does this feel rushed, scary or too good to be true?</li>
<li><strong>Check independently.</strong> Call the company using the official number on your SIM card, bank card or their real website. Do not use the number from the suspicious message.</li>
<li><strong>Ask someone you trust.</strong> A friend or family member may spot a red flag you missed.</li>
