<h3>Learn the warning signs</h3>
<p>Almost every scam shows one or more <strong>red flags</strong>. If you see these signs, stop and check before acting. Scammers rely on speed and emotion. A few minutes of calm thinking can save you from losing money or personal information.</p>

<h3>The main red flags</h3>
<ul>
<li><strong>Urgency:</strong> "Act now!" "Your line will be blocked today!" "Reply within five minutes!" Pressure is designed to stop you thinking.</li>
<li><strong>Too good to be true:</strong> A prize, job or deal you never applied for, especially one promising large amounts of money for little effort.</li>
<li><strong>Asks for a PIN, password or OTP:</strong> No genuine bank, network or company will <em>ever</em> ask for these. Never share them.</li>
<li><strong>Asks you to pay to receive money or a prize:</strong> Real winnings never require an upfront fee, airtime transfer or "tax" payment.</li>
<li><strong>Strange sender or link:</strong> A normal-looking name but an odd phone number, or a link that is misspelled (for example <em>airtel-promo-claim.xyz</em>).</li>
<li><strong>Poor spelling and grammar</strong>, or a generic greeting such as "Dear Customer" when the sender should know your name.</li>
<li><strong>Threats:</strong> Warnings that you will be arrested, your account will be closed or your family will be harmed.</li>
</ul>

<h3>The golden rules</h3>
<ul>
<li><strong>Never share your PIN or OTP</strong> with anyone, for any reason.</li>
<li><strong>Slow down.</strong> A genuine matter can wait five minutes while you check.</li>
<li><strong>Verify independently.</strong> Do not use the number or link in the message. Call the official number on the back of your card, visit the company's real shop, or use a number you already trust.</li>
</ul>

