<h3>Why record keeping matters</h3>
<p>Many small businesses fail not because they lack customers, but because the owner does not know whether the business is actually making money. Record keeping gives you the answer. Every time you receive money, write it down. Every time you spend money — on paper, ink, transport, data or electricity — write it down. At the end of the month, compare your total income with your total expenses.</p>
<p>A simple Excel spreadsheet is perfect for this. Create columns for Date, Description, Income and Expense. Add a fifth column for Balance, using a formula that calculates your running total. At any moment you can see how much money the business has made and whether you can afford to buy new equipment or pay yourself.</p>

<h3>Registering and staying legal</h3>
<p>When your business starts to grow, consider making it official. In Zambia, small businesses can register with the Patents and Companies Registration Agency (PACRA) and obtain a Taxpayer Identification Number (TPIN) from the Zambia Revenue Authority (ZRA). Registering makes it easier to open a business bank account, apply for tenders and build trust with larger customers.</p>
<p>Even before you register, keep honest records and pay any taxes that apply to your situation. Being legal protects you from fines and shows customers that you are professional.</p>

<h3>Reinvesting in your business</h3>
