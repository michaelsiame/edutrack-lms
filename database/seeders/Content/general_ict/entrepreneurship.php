<h3>Pricing your work</h3>
<p>Many beginners charge too little and end up making no profit, or even losing money. To set a fair price, add up your costs, then add something for your time and a profit margin.</p>
<blockquote><p><strong>Price = your costs + your time + profit</strong></p></blockquote>
<p>For example, suppose you design and print a one-page CV. The paper and ink cost about K2. You spend thirty minutes typing, formatting and printing it, and you value your time at K8. You add K3 profit for your skill. A fair price is therefore <strong>K13</strong>. If a competitor charges K15, you are still competitive. If a competitor charges K8, you need to explain why your quality is worth more, or find a way to reduce your costs.</p>

<h3>Keep simple records</h3>
