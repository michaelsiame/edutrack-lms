</ol>

<h3>Key terms</h3>
<ul>
<li><strong>Netiquette</strong>: Good manners and respectful behaviour when communicating online.</li>
<li><strong>Cyberbullying</strong>: Using digital tools to harass, insult, threaten or embarrass another person.</li>
<li><strong>Defamation</strong>: Making a false statement that damages someone's reputation.</li>
<li><strong>Bystander</strong>: A person who sees something happening but is not directly involved.</li>
</ul>

<h3>Summary</h3>
<p>Good netiquette is about remembering the human being behind every screen. Polite language, careful forwarding and refusal to participate in cyberbullying make the internet safer and more useful for everyone. Your manners online are just as important as your manners in person.</p>
