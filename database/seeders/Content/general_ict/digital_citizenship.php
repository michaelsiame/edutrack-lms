<h3>Protect your privacy</h3>
<p>Your personal information is valuable. Details such as your National Registration Card number, phone number, home address, date of birth, bank details and photos can be used by criminals to steal your money, impersonate you or trick your friends. Once you give this information away online, you cannot take it back. Share it only with people and services you trust.</p>
<p>Take time to review your privacy settings on Facebook, WhatsApp and any other platform you use. On Facebook, limit who can see your posts, photos and friend list. On WhatsApp, decide who can see your profile photo, status and "last seen" information. Be careful about posting your location in real time, especially if your home or daily movements become predictable.</p>
<ul>
<li>Never share your NRC, bank card or mobile-money details in a chat or email.</li>
<li>Be careful what you post about other people, especially children.</li>
<li>Do not fill in online forms asking for personal details unless you are sure the website is legitimate.</li>
</ul>

<h3>Strong passwords</h3>
<p>A password is the key to your digital life. A weak password is like leaving your front door open. A strong password is long, mixes letters, numbers and symbols, and is not easy to guess from your name, birthday or favourite football team.</p>
<ul>
<li>Use at least eight characters, and preferably twelve or more.</li>
<li>Mix uppercase letters, lowercase letters, numbers and symbols — for example <strong>Kalomo#2026!</strong>.</li>
<li>Do not use "1234", "password", your phone number or your child's name.</li>
<li>Use a different password for your most important accounts, especially email and mobile money.</li>
<li>Never share your password or one-time PIN (OTP) with anyone, including someone who claims to be from your bank or network provider.</li>
</ul>
<p>If remembering many passwords is hard, write them down and store the paper in a safe place at home. Do not save passwords in a note on your phone without a lock screen.</p>

<h3>Telling real information from fake</h3>
<p>Not everything you read online is true. Fake news, rumours and scams spread quickly because they are designed to make you react emotionally before you think. Before you believe or share something, check it carefully.</p>
<ul>
<li><strong>Who said it?</strong> Is it a known, trusted source such as a real news organisation, a government department or an official company page?</li>
<li><strong>Is it reported elsewhere?</strong> If only one strange website or WhatsApp group is sharing the story, be suspicious.</li>
<li><strong>Does it sound too shocking or too good to be true?</strong> Messages promising instant money, free airtime or miraculous cures are usually traps.</li>
<li><strong>Check the date.</strong> Old news is often reshared as if it is new.</li>
</ul>

