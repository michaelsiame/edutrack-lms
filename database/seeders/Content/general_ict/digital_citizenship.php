<h3>Treat people the way you would in person</h3>
<p><strong>Netiquette</strong> is short for "internet etiquette" — the good manners we use when we communicate online. The golden rule is simple: there is a real person on the other side of the screen, with feelings, responsibilities and challenges, just like you. Treat them the way you would if you were standing in front of them at the market, in church or at a college meeting.</p>
<p>It is easy to forget this because screens make us feel anonymous or distant. A person who would never shout at a stranger in person may feel comfortable typing insults in a comment section. A person who would never spread gossip at a funeral may forward unverified voice notes on WhatsApp without thinking. Good netiquette reminds us that digital communication is still human communication.</p>

<h3>Simple rules of netiquette</h3>
<ul>
<li><strong>Be polite.</strong> Say please and thank you, even in a quick WhatsApp message. Greet people properly before asking for help.</li>
<li><strong>Do not SHOUT.</strong> Typing in all capital letters feels like shouting and can seem aggressive. Use capitals only for short emphasis.</li>
<li><strong>Do not spread gossip or insults.</strong> Forwarding a hurtful message makes you part of the problem, even if you did not start it.</li>
<li><strong>Respect people's time.</strong> Keep group messages relevant. Avoid endless "good morning" forwards and chain messages in work or study groups.</li>
<li><strong>Check before you forward.</strong> Do not share news, photos, health advice or voice notes unless you know they come from a trustworthy source.</li>
<li><strong>Use clear language.</strong> Avoid confusing slang and abbreviations in professional or educational messages.</li>
</ul>

<h3>Cyberbullying and online harassment</h3>
<p>Using the internet to insult, threaten, embarrass or exclude someone is called <strong>cyberbullying</strong>. It is harmful, and in Zambia it can also break the law under provisions related to harassment, defamation and the misuse of communication networks. If someone bullies you online, do not reply in anger. Save evidence such as screenshots, block the person and tell a trusted adult, teacher, employer or the police if the situation is serious.</p>
<p>If you see someone else being bullied, do not join in and do not silently share the content. Report it and, when it is safe, offer support to the person being targeted. Bystanders who stay silent allow bullying to continue.</p>

