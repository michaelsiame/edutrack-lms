<h3>What is digital citizenship?</h3>
<p>A <strong>digital citizen</strong> is someone who uses computers, phones and the internet in a safe, respectful and responsible way. Just like being a good citizen of Zambia means following the law, respecting your neighbours and contributing to your community, being a good digital citizen means behaving well in the online spaces you share with millions of other people. Every email you send, every WhatsApp status you post, every comment you leave on Facebook and every photo you upload becomes part of your life online.</p>
<p>Digital citizenship is not about being perfect. It is about being thoughtful. Before you act online, ask yourself: <em>Is this safe? Is this kind? Is this honest? Would I be happy if my parents, my pastor or my future employer saw this?</em> If the answer to any of those questions is no, pause and choose a better action.</p>

<h3>Your digital footprint</h3>
<p>Everything you do online leaves a trail called your <strong>digital footprint</strong>. This footprint can last for years, even after you delete an app or close an account. Search engines, social media platforms and other people can find things you posted long ago. A silly photo from a party, an angry comment written in a hurry or a joke that seemed funny at the time can resurface when you apply for a job, try to join a professional group or run for a community position.</p>
<ul>
<li>Employers and college admissions officers often search for a person's name before making a decision.</li>
<li>Clients who want to hire you for computer work may look at your Facebook profile to see whether you appear professional.</li>
<li>Information you share about friends and family can also affect their reputations, not only yours.</li>
</ul>

