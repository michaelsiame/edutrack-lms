<p>Technology gives you powerful tools: you can learn, communicate, start a business and express your opinions. But with these tools come responsibilities. In Zambia, as in most countries, the law applies to your online behaviour just as it does to your offline behaviour. Using a phone or computer does not make illegal actions legal.</p>
