<p>Your online identity is the picture of you that the internet shows to the world. You can shape it deliberately. Start by using a clear, friendly profile photo and a name people can recognise. Fill in your bio with honest information about your skills, education and interests. If you have completed a course or achieved something worth sharing, post about it in a respectful way. For example, you might write: "Happy to have completed my Certificate in General Basic ICT & Computing at Edutrack College. I am now offering typing, spreadsheet and poster-design services in Kalomo."</p>
<p>Avoid posting content that is angry, hateful, violent or sexually suggestive. Do not join online mobs that attack or humiliate others. Remember that screenshots can be saved and shared without your permission. A message you send in private can become public in seconds.</p>

